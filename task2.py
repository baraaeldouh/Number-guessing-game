num1=int(input("enter first number"))
num2=int(input("enter second number"))
oper=input("choose an operation (+,-,/,*)")
if oper == "+":
    result=num1+num2
    print(num1,"+" ,num2,"=",result)
elif oper=="-":
    result=num1-num2
    print(num1,"-" ,num2,"=",result)
elif oper=="*":
    result=num1*num2
    print(num1,"*" ,num2,"=",result)





    
